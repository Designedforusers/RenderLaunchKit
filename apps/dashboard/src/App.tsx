export default function App() {
  // `useLocation()` lets AnimatePresence diff routes by pathname, so
  // navigating between Home and Project Detail produces a clean
  // cross-fade instead of a jarring DOM swap.
  const location = useLocation();

  return (
    <AnimatePresence mode="popLayout">
      <motion.div
        key={location.pathname}
        initial={false}
        animate={{ opacity: 1, y: 0, pointerEvents: 'auto' as const }}
        exit={{ opacity: 0, y: -8, pointerEvents: 'none' as const }}
        transition={{ duration: 0.32, ease: [0.16, 1, 0.3, 1] }}
      >
        <ErrorBoundary>
          <Routes location={location}>
            <Route path="/" element={<LandingPage />} />
            <Route path="/app" element={<HomePage />} />
            <Route path="/trends" element={<TrendsPageWrapper />} />
            <Route path="/create" element={<CreatePageWrapper />} />
            <Route path="/projects/:id" element={<ProjectPage />} />
            <Route path="*" element={<Navigate to="/" replace />} />
          </Routes>
        </ErrorBoundary>
      </motion.div>
    </AnimatePresence>
  );
}
